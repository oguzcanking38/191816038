    public void yukle(){
        int a;
        try {
            pst = con.prepareStatement("select * from urun");
            ResultSet rs = pst.executeQuery();
            
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i=1; i<=a; i++){
                v2.add(rs.getString("id"));
                v2.add(rs.getString("urunadi"));
                v2.add(rs.getString("adet"));
                v2.add(rs.getString("maliyet"));
                v2.add(rs.getString("barkod"));
                }
                df.addRow(v2);
            }
        } 
        catch (SQLException ex) {
            Logger.getLogger(satici.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void para(){
        try {
            pst = con.prepareStatement("Select para from ayarlar");
            
            ResultSet rs2 = pst.executeQuery();
            if(rs2.next()){
            
            lblpara.setText(rs2.getString("para"));
            para=Integer.parseInt(rs2.getString("para"));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(alissatis.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
            
            
            
    
    
    
    }
