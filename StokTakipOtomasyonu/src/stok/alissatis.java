    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        df=(DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(df.getValueAt(selected, 0).toString());
        int dbadet = Integer.parseInt(df.getValueAt(selected, 2).toString());
        int fiyat = Integer.parseInt(df.getValueAt(selected, 3).toString());
        try {
            String adet = textadet.getText();
            //adet alınır
            int i=Integer.parseInt(adet);
            //adet int olur
            int topadet = dbadet + i;
            //toplam adet bulunur
            topfiyat= fiyat*i;
            //alınan adet* fiyat
            paraduz = para-topfiyat;
            String adb = String.valueOf(topadet);
            pst = con.prepareStatement("update urun set adet=? where id=?");
            
            pst.setString(1, adb);
            
            pst.setInt(2, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Urun Satın Alındı");
            
            textadet.setText("");
            textadet.requestFocus();
            paraduzenleme();
            para();
            yukle();
        } catch (SQLException ex) {
            Logger.getLogger(satici.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

