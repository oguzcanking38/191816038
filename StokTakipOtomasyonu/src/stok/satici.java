    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        df=(DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        int id = Integer.parseInt(df.getValueAt(selected, 0).toString());
        try {
            String adi = txtsatici.getText();
            String tel = txttelefon.getText();
            String email = txtemail.getText();
            String adres = txtadres.getText();
            pst = con.prepareStatement("update satici set adi=?,telefon=?,email=?,adres=? where id=?");
            pst.setString(1, adi);
            pst.setString(2, tel);
            pst.setString(3, email);
            pst.setString(4, adres);
            pst.setInt(5, id);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Satici Eklendi");
            jButton1.setEnabled(true);
            txtsatici.setText("");
            txttelefon.setText("");
            txtemail.setText("");
            txtadres.setText("");
            txtsatici.requestFocus();
            yukle();
        } catch (SQLException ex) {
            Logger.getLogger(satici.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

