    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String adi = txtsatici.getText();
            String tel = txttelefon.getText();
            String email = txtemail.getText();
            String adres = txtadres.getText();
            pst = con.prepareStatement("insert into satici(adi,telefon,email,adres)values(?,?,?,?)");
            pst.setString(1, adi);
            pst.setString(2, tel);
            pst.setString(3, email);
            pst.setString(4, adres);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Satici Eklendi");
            txtsatici.setText("");
            txttelefon.setText("");
            txtemail.setText("");
            txtadres.setText("");
            txtsatici.requestFocus();
            yukle();
        } catch (SQLException ex) {
            Logger.getLogger(satici.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

